#XML Feeds
#
#Also available are our item XML feeds. Every item in the database has a corresponding XML feed. To reach those feeds, just add &xml to the end of any item url. For example:
#
#http://www.wowhead.com/?item=192 becomes http://www.wowhead.com/?item=192&xml
#
#Alternatively, you can use the actual name of the item (as long as it's spelled correctly). Like so:
#
#http://www.wowhead.com/?item=martin+thunder&xml
#
#These feeds contain a lot of information about items and can be used by anyone interested in developing 3rd-party applications.

